    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        calculadora calc = new calculadora();
        try {
            if (verificarValor(Double.parseDouble(txtNum1.getText())) || verificarValor(Double.parseDouble(txtNum2.getText()))) {
                double dobli = calc.calcula(Double.parseDouble(txtNum1.getText()), Double.parseDouble(txtNum2.getText()));
                jLabel2.setText("Resultado: " + dobli);
            } else {
                int inteiro = calc.calcula(Integer.parseInt(txtNum1.getText()), Integer.parseInt(txtNum2.getText()));
                jLabel2.setText("Resultado: " + inteiro);
            }
        } catch (Exception e) {
            String caractere = calc.calcula(txtNum1.getText(), txtNum2.getText());
            jLabel2.setText("Resultado: "+caractere);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

